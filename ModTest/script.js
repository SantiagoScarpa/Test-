function RaisingNumber(arrLenght, arr) {
    const modVal = mod()
    //console.log(modVal);

    let maxValue = 0;
    let minIndex = 0;

    for (let i = 0; i < arrLenght - 1; i++) {
        let j = arr[i + 1]
        let currVal = arr[i]

        let auxResult = currVal
        for (let aux = 1; aux <= j; aux++) {
            auxResult = auxResult * currVal
        }


        if (auxResult % modVal > maxValue % modVal) {
            minIndex = i
            maxValue = auxResult
        }
    }
    return minIndex

}

